# 正の整数を順に連結して得られる以下の10進の無理数を考える:
# 0.123456789101112131415161718192021...
# 小数第12位は1である.
# dnで小数第n位の数を表す.
# d1 × d10 × d100 × d1000 × d10000 × d100000 × d1000000 を求めよ.

# 脳死
res = ""
for i in range(1, 1000000 + 1):
    res += str(i)

result = int(res[1 - 1]) * int(res[10 - 1]) * int(res[100 - 1]) * \
    int(res[1000 - 1]) * int(res[10000 - 1]) * \
    int(res[100000 - 1]) * int(res[1000000 - 1])
print(result)
